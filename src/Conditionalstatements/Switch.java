package Conditionalstatements;

public class Switch {
    public static void main (String[] args){
        int day =8;
        switch (day){
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("tuesday");
                break;
            case 3:
                System.out.println("wednesday");
                break;
            case 4:
                System.out.println("thursday");
                break;
            default:
                System.out.println("bye bye ");
        }


    }
}
