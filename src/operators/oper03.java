package operators;

public class oper03 {
    public static void main(String[] args){
        // Assignment operator += ,-+ ,/=,*=
        int a=10;
        int b=20;
        int c =30;
        a+=10; // a=a+10
        b-=20; // b=b-20
        c*=30; // c=c*30
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}
